const config = require("../config");

module.exports = {
    cmd: "bot",
    alias: ["mainswitch", "botmode", "status"],
    desc: "Toggle bot's command processing engine",
    category: "OWNER",
    isOwner: true,
    async execute(conn, m, { text }) {
        const input = text?.toLowerCase();

        // 1. Check current status if no argument is provided
        if (input !== 'on' && input !== 'off') {
            const currentStatus = config.BOT_OFF === "true" ? "рҹ”ҙ кңұКҹбҙҮбҙҮбҙҳЙӘЙҙЙў (ЙӘЙўЙҙбҙҸКҖЙӘЙҙЙў бҙҳбҙңКҷКҹЙӘбҙ„)" : "рҹҹў бҙҖбҙ„бҙӣЙӘбҙ бҙҮ (бҙҳбҙңКҷКҹЙӘбҙ„)";
            return m.reply(
                `в•ӯв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җвҠ·\n` +
                `в•‘   рҹӨ– *рқҗҸрқҗҺрқҗҸрқҗҠрқҗҲрқҗғ-рқҗҢрқҗғ рқҗ„рқҗҚрқҗҶрқҗҲрқҗҚрқҗ„* \n` +
                `в• в•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җвҠ·\n` +
                `в•‘ рҹ“Ҡ *кңұбҙӣбҙҖбҙӣбҙңкңұ:* ${currentStatus}\n` +
                `в•‘ рҹ“қ *бҙңкңұбҙҖЙўбҙҮ:* .bot on | off\n` +
                `в•°в•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җвҠ·\n` +
                `> бҙЎКңбҙҮЙҙ 'бҙҸкң°кң°', бҙҸЙҙКҹКҸ бҙҸбҙЎЙҙбҙҮКҖкңұ бҙ„бҙҖЙҙ бҙңкңұбҙҮ КҷбҙҸбҙӣ. рҹҮ°рҹҮӘ`
            );
        }

        // 2. Process the toggle accurately
        if (input === 'off') {
            config.BOT_OFF = "true";
            process.env.BOT_OFF = "true";
            await m.react("рҹҳҙ");
            return m.reply(`вңЁ *рқҗҸрқҗҺрқҗҸрқҗҠрқҗҲрқҗғ-рқҗҢрқҗғ рқҗ’рқҗӢрқҗ„рқҗ„рқҗҸ рқҗҢрқҗҺрқҗғрқҗ„* вңЁ\n\nрҹ”ҙ *КҷбҙҸбҙӣ ЙӘкңұ ЙҙбҙҸбҙЎ ЙӘЙҙ кңұЙӘКҹбҙҮЙҙбҙӣ бҙҚбҙҸбҙ…бҙҮ.*\nЙӘбҙӣ бҙЎЙӘКҹКҹ ЙӘЙўЙҙбҙҸКҖбҙҮ бҙҖКҹКҹ бҙ„бҙҸбҙҚбҙҚбҙҖЙҙбҙ…кңұ кң°КҖбҙҸбҙҚ ЙҙбҙҸЙҙ-бҙҸбҙЎЙҙбҙҮКҖкңұ.`);
        }

        if (input === 'on') {
            config.BOT_OFF = "false";
            process.env.BOT_OFF = "false";
            await m.react("рҹҡҖ");
            return m.reply(`вңЁ *рқҗҸрқҗҺрқҗҸрқҗҠрқҗҲрқҗғ-рқҗҢрқҗғ рқҗҖрқҗӮрқҗ“ЙӘбҙ бҙҮ бҙҚбҙҸбҙ…бҙҮ* вңЁ\n\nрҹҹў *КҷбҙҸбҙӣ ЙӘкңұ ЙҙбҙҸбҙЎ бҙЎЙӘбҙ…бҙҮ бҙҖбҙЎбҙҖбҙӢбҙҮ.*\nбҙҖКҹКҹ бҙ„бҙҸбҙҚбҙҚбҙҖЙҙбҙ…кңұ бҙҖКҖбҙҮ бҙҖбҙ„бҙ„бҙҮкңұкңұЙӘКҷКҹбҙҮ бҙӣбҙҸ бҙҮбҙ бҙҮКҖКҸбҙҸЙҙбҙҮ.`);
        }
    }
};
